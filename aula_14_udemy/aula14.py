# para utilizar as aspas iguais em uma só sentença, utilizar a \ logo antes das aspas, como no exemplo abaixo
print("Para utilizar as \"\" em um texto sem que o python entenda a final da string, utiliza-se \ ")

# para trocar de linha no python, utilizar \n

print("Para quebrar uma linha, ou seja, para trocar de linha \n sem precisar apertar a tecla enter, basta usar "
      "a barra invertida seguida sem espaço da letra n")

# no exemplo acima, não pude mostrar \n pois o python entende como um comando, para mostrar ele, ou seja, para eu
# informar ao python que não quero que nada seja executado, nada seja interpretado como um comando, basta colocar
# a letra r na frente das aspas na hora do print

print(r"Para quebrar a linha, basta usar o comando \n")

